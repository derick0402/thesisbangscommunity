    <p id="disasterTitle">Ano nga ba ang Pagguho ng Lupa o Landslide?</p>
    <p class="descriptions">
        Nagaganap ang pagguho ng lupa o landslide sa mga burol at bundok dahil sa malakas
        o tuloy-tuloy na pag-ulan o dahil sa lindol. Posibleng bunga rin ito ng pagmimina, 
        paggawa ng kalsada, di-akmang paggamit ng lupa at pagputol ng mga puno sa kagubatan.
    </p>
    <p id="disasterTitle">Mga dapat gawin bago magka-landslide:</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Iwasan ang pagtayo ng bahay o anumang gusali sa matatarik na lugar o maging sa paanan ng bundok.
        </li>
        <li>
            Maging mapagmasid sa mga palatandaan ng pagguho ng lupa, lalo na kung malakas ang ulan o bagyo.
        </li>
        <li>
            Alamin ang sistema ng mga babala sa komunidad para sa lindol at mga dulot nitong 
            panganib gaya ng landslide.
        </li>
        <li>
            Kung naglalakbay sa mabundok na lugar, maging alerto sa mga babala sa tabi ng kalsada.
        </li>
        <li>
            Pag-aralan ang evacuation plan para sa inyong lugar kapag may banta ng pagguho ng lupa.
        </li>
        <li>
            Sumali sa mga pagsasanay hinggil sa pagbasa ng rail gauge sa iyong komunidad at maging 
            aktibo sa pagmo-monitor ng dami ng ulang bumabagsak.
        </li>
        <li>
            Pumunta sa evacuation center kapag inabisuhan ng mga awtoridad. 
        </li>
    </ul>
    <p id="disasterTitle">Mga dapat gawin habang mayroong landslide:</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Kung biglaan ang pagguho, lumikas agad.  
        </li>
        <li>
            Kung inabutan ka ng pagguho sa loob ng bahay, magtago sa ilalim ng matibay na mesa o bagay.  
        </li>
        <li>
            Kung bumibyahe o nasa labas ng bahay, agad na umalis sa lugar na pinangyarihan ng 
            pagguho dahil maaaring magkaroon pa ng kasunod ito.
        </li>
        <li>
            Ipagbigay-alam agad sa mga awtoridad ang nangyaring pagguho sa inyong lugar.
        </li>
    </ul>
    <p id="disasterTitle">Mga dapat gawin pagkatapos ng landslide:</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Makinig sa anunsyo o balita tungkol sa mga kaganapan kaugnay ng pagguho.         
        </li>
        <li>
            Siguraduhing ligtas nang bumalik sa tirahan bago ito gawin.
        </li>
    </ul>
    <p id="disasterTitle">Sinkhole</p>
    <p class="descriptions">
        Ang sinkhole o uka sa lupa ay isang malaking butas sa ibabaw na bahagi ng lupa 
        na biglaang lumilitaw, maaring sa kanayunan o sa lungsod man. 
        Karaniwang di lumalampas sa 30 metro ang lalim nito. Ito ay kadalasang bunga ng 
        pagkatunaw ng namumuong malambot na mga bato at mineral sa ilalim ng lupa gaya ng asin, 
        limestone at gypsum. Minsan, may tubig sa ilalim ng lupa na tumutunaw sa bedrock dito. 
        Dahil sa init ng panahon, maaaring magbitak ang lupa sa ibabaw at maging sinkhole ito. 
        May mga nsinkhole rin na bunga ng paghuhukay, tulad ng pagmimina.
    </p>
    <p id="disasterTitle">Sinyales ng Sinkhole</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Pagkawala sa tamang lapat ng pinto o bintana.               
        </li>
        <li>
            Mga bitak sa pader o konkretong poste ng bahay o gusali.
        </li>
        <li>
            Lumulubog na bahagi ng sahig, bakod o buong bahay.
        </li>
        <li>
            Aktuwal na pagbibitak ng lupa.    
        </li>
        <li>
            Pabilog na hugis sa nalalantang damuhan.  
        </li>
        <li>
            Lumulubog na kalsada at pagbibiyak ng konkreto nito.       
        </li>
    </ul>
