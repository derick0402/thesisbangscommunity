<div id="disaster">
    <p id="disasterTitle">Ano ba ang Sunog?</p>
    <p class="descriptions">
        Ang sunog ay kadalasang nangyayari tuwing tag-init, lalo na kapag buwan ng Marso. 
        Kung kaya idineklara ito ng gobyerno ng Pilipinas bilang Buwan ng Pag-iwas sa Sunog o 
        Fire Prevention Month. Ayon sa Presidential Proclamation No. 115-A layunin ng taunang 
        pagdiriwang na ito na itaas ang antas ng kamalayan ng publiko upang makaiwas sa sunog 
        at masagip ang inosenteng mga buhay at mahahalagang ari-arian na kadalasang nadadamay 
        at nasisira dahil sa mga ganitong trahedya.
    </p>
    <p id="disasterTitle">Mga dapat gawin bago magkasunog:</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Magplano at sanayin ang pamilya dalawang beses sa isang taon sa 
            mga dapat gawin sakaling magkasunog. Gawin pati na ang pagsasanay sa 
            pagkapa ng daan palabas sa gitna ng dilim o paggapang palabas.
        </li>
        <li>
            Makiisa sa mga pagsasanay o fire drills, sa bahay man, paaralan, 
            pamilihan o lugar na pinagtatrabahuhan.
        </li>
        <li>
            Ipaalala sa mga kasamahan lalo na sa mga bata na humingi ng tulong sa awtoridad
            tulad ng bumbero kung nasa peligro ng sunog. Turuan din ang mga bata na huwag matakot
            at pagtaguan ang mga bumbero.
        </li>
        <li>
            Iwasan ang pagsisiga o pag-iipon ng mga tuyong dahoon o papel malapit sa bahay 
            na maaaring pagmulan ng apoy. Huwag magtago sa loob ng bahay ng mga materyales na 
            madaling magliyab gaya ng gasolina, alcohol o pintura.
        </li>
        <li>
            Iwasang gumamit ng mga lusis o anong mang uri ng paputok.
        </li>
        <li>
            Maglagay ng mga signage ng fire exit o labasan sakaling magkasunog. Makakabuti kung 
            may dalawa o higit pang lagusan palabas sa isang bahay o gusali.
        </li>
        <li>
            Ipaskil sa madaling makita ang mga numero ng mga ahensyang dapat tawagan sa anumang
            emergency, gaya ng bumbero.
        </li>
        <li>
            Pagasamasamahin ang mga mahahalagang papeles, alahas at iba pa sa isang 
            lalagyan para sa madaliang paglikas.
        </li>
    </ul>
    <p id="disasterTitle">Mga dapat gawin habang may sunog:</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Huwag magpanik. Panatilihing kalmado hanggang sa marating ang exit. 
        </li>
        <li>
            Gamitin ang fire extinguisher, o buhangin sa pag-apula sa apoy kung hindi
            pa ito kalakihan. Kapag tumagal o umabot na sa kisame ang apoy, agaran ng lumikas.
        </li>
        <li>
            Magsuot ng matibay na sapin sa paa upang maprotektahan ang sarili sa mga matatapakang
            nagbabagang bagay, bubog at iba pa.
        </li>
        <li>
            Hindi ligtas ang magtago sa banyo. Dahil makakapasok pa rin ang nakakamatay
            na usok sa iyong kinalalagyan.        
        </li>
        <li>
            Umiwas sa makapal na usok. Takpan ang bibig at ilong ng basang tela at mabilis na 
            gumapang palabas.
        </li>
        <li>
            Protektahan ang sarili mula sa apoy habang mabilis na lumilikas. Takluban ang buong 
            katawan ng basang kumot, tuwalya o ano mang tela. Siguraduhing natatakpan ang 
            ilong at bibig upang hindi makalangghap ng usok.
        </li>
        <li>
            Huwag mag-atubiling sumigaw para humingi ng tulong.
        </li>
        <li>
            Kung nasusunog ang iyong damit, agarang huminto, dumapa ata gumulong upang mapuksa ang apoy.  
        </li>
    </ul>
    <p id="disasterTitle">Mga dapat gawin pagkatapos may sunog:</p>
    <ul class="subUl" style="font-weight:lighter">
        <li>
            Ipaubaya sa mga kinauukulan ang pagkukumpuni ng mga linya ng kuryente, tubig, at telepono.
        </li>
        <li>
            Maingat na alamin ang mga pinsalang idinulot ng sunog sa inyong bahay.
        </li>
        <li>
            Tiyaking ligtas nang bumalik sa tahanan bago pumasok dito. Makinig sa sasabihin ng bumbero.        
        </li>
        <li>
            Makipag-ugnayan sa kinauukulang ahensya ng pamahalaan para sa hindi naisalbang dokumento.
        </li>
        <li>
            Makipag-ugnayan sa barangay o mga organisasyong nagkakawanggawa, gaya ng Philippine 
            Red Cross kung nangangailangan ng pagkain, damit, gamot at pansamantalang tirahan.
        </li>
        <li>
            Huwag nang kainin ang anumang pagkain sa lata o inumin na nalantad sa sunog.    
        </li>
    </ul>
