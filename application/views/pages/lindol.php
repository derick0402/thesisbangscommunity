    <p id="disasterTitle">Ano nga ba ang lindol?</p>
    <p class="descriptions">Ito ay ang pag-alog ng lupa 
        bunga ng biglaang paggalaw ng mga 
        bato sa ilalim nito. Ito ay maaring mahina hanggang 
        sa malakas na nakapagdudulot ng iba’t – ibang pinsala o 
        panganib gaya ng pagyanig o ground shaking, pagbitak ng lupa o 
        rupture, paglambot ng lupa o liquefaction, pagguho ng lupa o landslide o 
        malahiganteng alon o tsunami.
    </p>
    <p id="disasterTitle">Mga dapat gawin bago magka-lindol:</p>
    <ul >
        <li>Alamin kung ang inyong lugar ay 
        delikado sa lindol. Kung hindi maiiwasan 
        ang pagtatayo ng bahay, sundin ang nararapat na disenyo para dito.</li>
        <li>Lagyan ng suhay (brace) ang matataas na gamit 
        gaya ng aparador o kabinet para hindi mabuwal at italing 
        mabuti ang mga nakasabit upang hindi bumagsak.</li>
        <li>Suriin at patibayin ang mga bahagi ng gusali gaya ng poste, pader at bubong.</li>
        <li>Ilagay sa pinakamababang lugar ng kabinet ang mga
         nakalalason at lumiliyab na kemikal. Siguraduhing ito ay
        may lapat at mahigpit na takip.</li>
        <li>Itabi ang mahahalagang dokumento sa ligtas na lugar 
        at madaling makuha sa oras ng kagipitan.</li>
        <li>Alamin ang mga alternatibong lagusan bukod sa karaniwang 
        daanan palabas ng bahay, gusali, paaralan o kung saan naroroon.</li>
    </ul>
    <p id="disasterTitle">Mga dapat gawin habang lumilindol:</p>
    <strong class="descriptions">A. Kung nasa loob ng gusali o istraktura: </strong>
    <br/>
    <strong class="mainSubDescriptions">
        Maging kalmado at huwag mataranta o mag-panic. 
        Gawin lamang ang sumusunod upang maprotektahan agad ang sarili:
    </strong>
    <ul class="subUl">
        <li><strong>DUMAPA</strong> (Drop) - dumapa sa sahig upang hindi matumba.</li>
        <li><strong>MAGTAGO</strong> (Cover) - magtago sa ilalim ng matibay na bagay kagaya ng lamesa.</li>
        <li><strong>KUMAPIT</strong> (Hold On) - kumapit ng maigi sa matibay na bagay.</li>
    </ul>
    <strong class="mainSubDescriptions">
        Kung walang mapagkukublihan, isagawa ang "TRIPOD POSITION":
    </strong>
    <ul class="subUl">
        <li>
            Lumuhod sa isang tuhod habang nakaapak ang kabilang paa sa sahig.
        </li>
        <li>
            Ilapat nang mariin ang isang kamay sa matibay na sahig o pader para mapanatili ang balanse.
            Protektahan ang sarili gamit ang isa pang kamay, takpan ang ulo o salagin ang mga nahuhulog 
            na bagay.
        </li>
        <li>
            Patayin ang nakabukas na kalan at iba pang gamit na may apoy.
        </li>
    </ul>
    <strong class="descriptions">B. Kung nasa labas ng bahay o gusali: </strong>
    <ul class="subUl">
        <li>
            Lumayo sa tabing ilog at sa bangin o matataas na lugar na maaaring maapektuhan
            ng pagguho ng lupa
        </li>
        <li>
            Mag ingat sa bumabagsak na mga bagay. Lumayo sa mga puno, linya ng kuryente, mga poster at
            konkretong pader.
        </li>
    </ul>
